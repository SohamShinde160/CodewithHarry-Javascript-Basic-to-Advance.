// DOM tree refers to the HTML page where all nodes are converted into objects
//  there are 3 main types of nodes in DOM tree:
//       1. text node
//       2. element node
//       3. comment node
// DOM TREE :- your html is being converted into javscript object(document) & now you can now use it as an object.
// in an html page :- <html> tag is root
//                    <head> & <body> tag is children
//                    text node is always leaf node.     (<title> <p> <h1> etc)

//  Auto Correction
// if an errorness HTML is encountered by the browser it tends to correct it.
//   for eg :- if we put something after the body tag , it is automatically moved inside the body tag
//             <table> tag must contain <tbody> tag


//  walking the DOM
//  document.body -------> body element of your html code will be displayed in console    (typeof --> object)
//  document.head -------> head element of your html code will be displayed in console    (typeof --> object)
//  document.title -------> title tag of your html code will be displayed in console  (typeof --> "string")
//  document.documentElement ---------> it will full html page in console tab         (typeof --> object)

 
